# 1.1 - DIVISION CON RESTO

# Plantear un script que permita informar, para dos valores de a y b, el resultado de la division a/b y el resto de esa division

a = int(input("Ingrese el valor de a: "))
b = int(input("Ingrese el valor de b: "))

print(f"El resultado de la division entre a y b es {a/b} y el resto de la division es {a%b}")

# Otra forma de mostrar el resultado

print('a/b = ', a/b, 'y el resto es ', a%b)



